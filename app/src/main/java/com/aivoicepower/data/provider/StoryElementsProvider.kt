        "відважний лицар", "хитрий шахрай", "добра відьма",
        "загублений студент", "таємничий незнайомець", "веселий музикант",
        "сором'язливий поет", "амбітний підприємець", "чарівний кіт"
    )

    // Місця дій (називний)
    private val places = listOf(
        "покинутий замок", "таємнича бібліотека", "шумний ринок",
        "тихий ліс", "сучасне кафе", "космічна станція",
        "підземне місто", "гірський притулок", "плавучий острів",
        "старовинний театр", "футуристична лабораторія", "зачарований сад"
    )

    // Місця дій (місцевий відмінок — для "у/в/на ...")
    private val placesLocative = listOf(
        "у покинутому замку", "в таємничій бібліотеці", "на шумному ринку",
        "у тихому лісі", "в сучасному кафе", "на космічній станції",
        "у підземному місті", "в гірському притулку", "на плавучому острові",
