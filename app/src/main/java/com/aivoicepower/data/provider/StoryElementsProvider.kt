        "кришталева куля", "стара фотографія", "незвичайний камінь"
    )

    // Предмети (знахідний відмінок — для "знаходить ...")
    private val itemsAccusative = listOf(
        "заржавілий ключ", "світну книгу", "старовинну карту",
        "дивну скриньку", "магічний амулет", "поламаний годинник",
        "таємничий лист", "золоту монету", "срібне дзеркало",
        "кришталеву кулю", "стару фотографію", "незвичайний камінь"
    )

    // Твісти/повороти подій
    private val twists = listOf(
        "раптом зникає світло", "з'являється портал у інший вимір",
