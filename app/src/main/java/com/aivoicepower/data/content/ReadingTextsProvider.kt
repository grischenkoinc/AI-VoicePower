object ReadingTextsProvider {

    fun getAllReadingTexts(): List<ReadingText> {
        return getNewsTexts() + getLiteraryTexts() + getMotivationalTexts() +
               getTechnicalTexts() + getDialogues()
    }

    fun getTextsByEmotion(emotion: Emotion): List<ReadingText> {
        return getAllReadingTexts().filter { it.emotion == emotion }
    }

    fun getTextsByDifficulty(difficulty: Int): List<ReadingText> {
        return getAllReadingTexts().filter { it.difficulty == difficulty }
    }

    fun getRandomTexts(count: Int): List<ReadingText> {
        return getAllReadingTexts().shuffled().take(count)
    }

