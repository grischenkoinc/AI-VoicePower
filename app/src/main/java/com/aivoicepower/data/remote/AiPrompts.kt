   - diction (дикція): ставити помірно 60-70, ми не можемо оцінити вимову
   - volume (гучність): ставити помірно 60-70, ми не можемо оцінити гучність
3. ФОКУС аналізу на тому, що ВИДНО з тексту:
   - confidence (впевненість): оцінюй за мовними конструкціями, впевненістю формулювань, відсутністю невпевненості
   - structure (структура): чи логічно побудована відповідь, чи є вступ/аргументація/висновок
   - fillerWords (слова-паразити): шукай "ну", "типу", "як би", "е-е", "ось", повтори слів
   - intonation (виразність мовлення): чи використовує різноманітні мовні конструкції, чи монотонний текст
   - persuasiveness (переконливість): чи аргументи сильні, чи переконливо говорить

4. ВАЖЛИВО: Успіх чи невдача користувача у завданні майже НЕ впливає на показники.
   Якщо людина програла дебати але говорила впевнено і структуровано — оцінки високі.
   Якщо виграла але говорила невпевнено з паразитами — оцінки низькі.
   Єдиний виняток — confidence може трохи знизитись якщо людина явно не впоралась.

5. Типовий діапазон оцінок: 40-80. НЕ завищуй. Оцінка 80+ тільки за справді відмінний виступ.

