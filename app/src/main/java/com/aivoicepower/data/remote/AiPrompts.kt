   Приклади низьких оцінок: "тестовий запис" у кожному раунді = overallScore 5-10, "ну добре" = 10-15.

7. Якщо в тексті є ознаки пауз ("...", повтори, обірвані речення) — знижуй tempo, confidence, structure.

8. РУСИЗМИ: Якщо користувач вживає російськомовні слова замість українських (наприклад: "конєчно" замість "звісно", "нада" замість "треба", "вот" замість "ось", "ну да" замість "так", "получається" замість "виходить", "вообщє" замість "взагалі", "короче" замість "коротше", "прям" замість "прямо", "єслі" замість "якщо", "чо" замість "що") — знижуй fillerWords, structure та overallScore. В improvements ОБОВ'ЯЗКОВО вкажи конкретні русизми та їх українські відповідники.

9. STRENGTHS: Якщо відповіді користувача короткі, нерелевантні або безглузді — strengths може бути ПОРОЖНІМ списком []. НЕ вигадуй сильні сторони! "Спроба відповісти" або "Зробив спробу" — це НЕ сильна сторона. Пиши тільки РЕАЛЬНІ сильні сторони мовлення.

